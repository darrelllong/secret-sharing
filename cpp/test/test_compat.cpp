// Cross-language bit-compatibility tests.
//
// The Rust crate at the repository root ships an example,
// `examples/dump_compat_vectors.rs`, that emits deterministic test
// vectors covering the operations both implementations must agree
// on byte-for-byte. The vectors below are a copy of that output;
// the script `cpp/scripts/regen_compat.sh` regenerates them.
//
// What's covered:
// - ChaCha20 keystream from a fixed 32-byte seed (first 256 bytes).
// - BigUint multiplication: a * b for several operand pairs, with
//   the product encoded as big-endian bytes — so a transcription
//   error in the C++ port's limb layout is caught even if the
//   numerical result happens to match.
// - Shamir share reconstruction: given the share `(x, y)` byte
//   strings produced by the Rust impl on a fixed seed, the C++
//   reconstruct must recover the same secret.
//
// Anything in this file must remain bit-identical with what the
// Rust dump_compat_vectors example produces. Run the regen script
// after intentional changes.

#include "secret_sharing/bigint.hpp"
#include "secret_sharing/csprng.hpp"
#include "secret_sharing/field.hpp"
#include "secret_sharing/poly.hpp"
#include "secret_sharing/shamir.hpp"

#include <gtest/gtest.h>

#include <array>
